# DNA 定义
DNA_PROFILES = {
    "luxun": """
风格特征：犀利、数据驱动、强观点

核心特点：
1. 开门见山，不绕弯子
2. 大量使用数据和图表
3. 观点鲜明，敢于批判
4. 逻辑严密，论证充分
5. 语言简洁，不煽情

适合场景：财经分析、行业观察、政策解读

写作要点：
- 多用短句，节奏快
- 多用数据，少用形容词
- 多用"实际上"、"本质上"、"关键在于"
- 避免"可能"、"也许"、"大概"等模糊词
- 金句要有力度，不要温吞
""",

    "lemon": """
风格特征：温和、叙事性强、情感共鸣

核心特点：
1. 娓娓道来，像讲故事
2. 注重情感和共鸣
3. 观点温和，包容多元
4. 多用比喻和类比
5. 语言生动，有画面感

适合场景：生活方式、文化内容、人物故事

写作要点：
- 多用长句，节奏舒缓
- 多用细节，少用数据
- 多用"你会发现"、"就像"、"想象一下"
- 可以用"可能"、"也许"表达不确定性
- 金句要有温度，不要太硬
"""
}


def load_framework_guide(framework_name: str) -> str:
    """加载框架指南"""
    return FRAMEWORKS.get(framework_name, FRAMEWORKS["热点解读型"])


def load_strategy_guide(strategy_name: str) -> str:
    """加载策略指南"""
    return STRATEGIES.get(strategy_name, STRATEGIES["角度发现"])


def load_dna_guide(dna_name: str) -> str:
    """加载 DNA 指南"""
    return DNA_PROFILES.get(dna_name, DNA_PROFILES["luxun"])


def get_all_frameworks() -> list:
    """获取所有框架名称"""
    return list(FRAMEWORKS.keys())


def get_all_strategies() -> list:
    """获取所有策略名称"""
    return list(STRATEGIES.keys())


def get_all_dnas() -> list:
    """获取所有 DNA 名称"""
    return list(DNA_PROFILES.keys())


if __name__ == "__main__":
    # 测试
    print("可用框架：", get_all_frameworks())
