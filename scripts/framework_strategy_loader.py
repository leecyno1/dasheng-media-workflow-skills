    print("\n可用 DNA：", get_all_dnas())

    print("\n\n=== 痛点型框架 ===")
    print(load_framework_guide("痛点型"))

    print("\n\n=== 密度强化策略 ===")
    print(load_strategy_guide("密度强化"))

