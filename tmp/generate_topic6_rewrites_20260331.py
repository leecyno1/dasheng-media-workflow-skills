def count_chars(text: str) -> int:
    return len(re.sub(r"\s+", "", text))


def clean_artifact_text(text: str) -> str:
    cleaned = text.replace("\r", "")
    cleaned = cleaned.replace("+\n", "\n").replace("\n+", "\n")
    cleaned = re.sub(r"[ \t]+\n", "\n", cleaned)
    cleaned = re.sub(r"\n{3,}", "\n\n", cleaned)
    return cleaned.strip() + "\n"


def extend_to_min_chars(text: str, min_chars: int, appendix_blocks: list[str]) -> str:
    output = text
    index = 0
    while count_chars(output) < min_chars:
        if not appendix_blocks:
            break
        output = output.strip() + "\n\n" + appendix_blocks[index % len(appendix_blocks)].strip() + "\n"
        index += 1
        if index > 20:
            break
    return output


